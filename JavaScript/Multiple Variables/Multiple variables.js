// You can declare multiple variables in the same line.

let number1, number2, number3;

number1 = 2;
number2 = 4;
number3 = 6;

document.write(number1);
document.write(" / " + number2);
document.write(" / " + number3);

// Also you can:

let number4 = 8, number5 = 7, number6 = 10;

document.write(" / " + number4);
document.write(" / " + number5);
document.write(" / " + number6);

// And: 

let number7 = 15;
let number8 = 20;
let number9 = 16;

document.write(" / " + number7);
document.write(" / " + number8);
document.write(" / " + number9);